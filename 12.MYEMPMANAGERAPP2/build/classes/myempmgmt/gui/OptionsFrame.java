    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
         boolean result=validateInputs();
         if(!result){
         JOptionPane.showMessageDialog(null, "Plese select an option first ");
         return;
         }
         JFrame frame=null;
         if(jrbAddEmp.isSelected()){
          frame=new AddEmployeeFrame();
         //  addEmp.setVisible(true);        // AddEMployee wala frame open ho jayega
          // this.dispose();                // es frame ko dispose kr dega 
         }
        else if(jrbSearchEmp.isSelected()){
           frame=new SearchEmployeeFrame();
          
         }
        else  if(jrbShowAllEmp.isSelected()){
         frame=new ViewAllEmployeeFrame();
         }
          else{
          DBConnection.closeConnection();
          System.exit(0);
          }
         frame.setVisible(true);
         this.dispose();
          
    }//GEN-LAST:event_btnDoTaskActionPerformed

